void wii_ext_crypto_init(const uint8_t key_data[16])
{
    // The 16-byte key is split:
    //   key[0-5]  = key_data[0x40-0x45] relative to reg base = key_data[0-5]
    //   rand[0-9] = key_data[0x46-0x4F] relative to reg base = key_data[6-15]
    const uint8_t* key  = &key_data[0];   // 6 bytes
    const uint8_t* rand = &key_data[6];   // 10 bytes

    // Derive ft (first transformation table) from S-box lookups
    ft[0] = sbox_a[key[4]]  ^ sbox_b[rand[3]];
    ft[1] = sbox_a[key[2]]  ^ sbox_b[rand[5]];
    ft[2] = sbox_a[key[5]]  ^ sbox_b[rand[7]];
    ft[3] = sbox_a[key[0]]  ^ sbox_b[rand[2]];
    ft[4] = sbox_a[key[1]]  ^ sbox_b[rand[4]];
    ft[5] = sbox_a[key[3]]  ^ sbox_b[rand[9]];
    ft[6] = sbox_a[rand[0]] ^ sbox_b[rand[6]];
    ft[7] = sbox_a[rand[1]] ^ sbox_b[rand[8]];

    // Derive sb (second transformation table) from S-box lookups
    sb[0] = sbox_a[key[0]]  ^ sbox_b[rand[1]];
    sb[1] = sbox_a[key[5]]  ^ sbox_b[rand[4]];
    sb[2] = sbox_a[key[3]]  ^ sbox_b[rand[0]];
    sb[3] = sbox_a[key[2]]  ^ sbox_b[rand[9]];
    sb[4] = sbox_a[key[4]]  ^ sbox_b[rand[7]];
    sb[5] = sbox_a[key[1]]  ^ sbox_b[rand[8]];
    sb[6] = sbox_a[rand[3]] ^ sbox_b[rand[5]];
    sb[7] = sbox_a[rand[2]] ^ sbox_b[rand[6]];

    wii_ext_crypto_enabled = true;
    printf("[wii_ext_crypto] Encryption enabled (ft=%02X%02X%02X%02X sb=%02X%02X%02X%02X)\n",
           ft[0], ft[1], ft[2], ft[3], sb[0], sb[1], sb[2], sb[3]);
}

void wii_ext_crypto_encrypt(uint8_t* data, uint8_t addr, uint8_t len)
{
    for (uint8_t i = 0; i < len; i++, addr++) {
        data[i] = (data[i] - ft[addr % 8]) ^ sb[addr % 8];
    }
}

void wii_ext_crypto_decrypt(uint8_t* data, uint8_t addr, uint8_t len)
{
    // Inverse of encrypt: plaintext = (ciphertext ^ sb) + ft
    for (uint8_t i = 0; i < len; i++, addr++) {
        data[i] = (data[i] ^ sb[addr % 8]) + ft[addr % 8];
    }
}

// ---- Self-test --------------------------------------------------------------
//
// Known-answer test using a key captured from a real Wii + Wiimote session
// and expected values computed independently against Dolphin's reference
// (see prior research). If any step fails, the bug is inside this file
// (S-box data, derivation formula, or encrypt loop). If all steps pass,
// the cipher is correct and any analog-stick issues are NOT a crypto bug.

static void wii_ext_crypto_print_diff(const char* label,
                                      const uint8_t* got,
                                      const uint8_t* want,
                                      size_t n)
{
    printf("[wii_ext_crypto]   %s got : ", label);
    for (size_t i = 0; i < n; i++) printf("%02X ", got[i]);
    printf("\n[wii_ext_crypto]   %s want: ", label);
    for (size_t i = 0; i < n; i++) printf("%02X ", want[i]);
    printf("\n");
}

void wii_ext_crypto_self_test(void)
{
    // Standalone self-test — computes everything in locals, never touches
    // the module's ft/sb/enabled state. Safe to call anywhere, including
    // from inside wii_ext_crypto_init just before the real init runs.
    static const uint8_t test_key[6]  = {0x39, 0x1B, 0x5B, 0x41, 0x15, 0x5E};
    static const uint8_t test_rand[10] = {
        0xDF, 0xCA, 0x39, 0x1B, 0x6E, 0xFE, 0x16, 0x16, 0x62, 0xC3
    };
    static const uint8_t expected_ft[8] = {
        0x31, 0x3F, 0x0A, 0x3D, 0xA3, 0xBC, 0x92, 0x03,
    };
    static const uint8_t expected_sb[8] = {
        0xA9, 0xC2, 0x91, 0x24, 0xA9, 0xE5, 0x66, 0xFA,
    };
    static const uint8_t cal_plain[16] = {
        0xFC, 0x04, 0x7E, 0xFC, 0x04, 0x7E, 0xFC, 0x04,
        0x7E, 0xFC, 0x04, 0x7E, 0x00, 0x00, 0x4D, 0xA2,
    };
    static const uint8_t cal_cipher_expected[16] = {
        0x62, 0x07, 0xE5, 0x9B, 0xC8, 0x27, 0x0C, 0xFB,
        0xE4, 0x7F, 0x6B, 0x65, 0xF4, 0xA1, 0xDD, 0x65,
    };
    static const uint8_t data_plain[6]  = { 0xA0, 0x20, 0x10, 0x00, 0xFF, 0xFF };
    static const uint8_t data_cipher_expected[6] = {
        0xC6, 0x23, 0x97, 0xE7, 0xF5, 0xA6,
    };

    const uint8_t* k = test_key;
    const uint8_t* r = test_rand;
    uint8_t lft[8], lsb[8];

    lft[0] = sbox_a[k[4]] ^ sbox_b[r[3]];
    lft[1] = sbox_a[k[2]] ^ sbox_b[r[5]];
    lft[2] = sbox_a[k[5]] ^ sbox_b[r[7]];
    lft[3] = sbox_a[k[0]] ^ sbox_b[r[2]];
    lft[4] = sbox_a[k[1]] ^ sbox_b[r[4]];
    lft[5] = sbox_a[k[3]] ^ sbox_b[r[9]];
    lft[6] = sbox_a[r[0]] ^ sbox_b[r[6]];
    lft[7] = sbox_a[r[1]] ^ sbox_b[r[8]];
    lsb[0] = sbox_a[k[0]] ^ sbox_b[r[1]];
    lsb[1] = sbox_a[k[5]] ^ sbox_b[r[4]];
    lsb[2] = sbox_a[k[3]] ^ sbox_b[r[0]];
    lsb[3] = sbox_a[k[2]] ^ sbox_b[r[9]];
    lsb[4] = sbox_a[k[4]] ^ sbox_b[r[7]];
    lsb[5] = sbox_a[k[1]] ^ sbox_b[r[8]];
    lsb[6] = sbox_a[r[3]] ^ sbox_b[r[5]];
    lsb[7] = sbox_a[r[2]] ^ sbox_b[r[6]];

    bool ft_ok = (memcmp(lft, expected_ft, 8) == 0);
    bool sb_ok = (memcmp(lsb, expected_sb, 8) == 0);

    uint8_t cal_buf[16];
    memcpy(cal_buf, cal_plain, 16);
    for (int i = 0; i < 16; i++) {
        uint8_t a = (uint8_t)(0x20 + i);
        cal_buf[i] = (cal_buf[i] - lft[a & 7]) ^ lsb[a & 7];
    }
    bool cal_ok = (memcmp(cal_buf, cal_cipher_expected, 16) == 0);

    uint8_t data_buf[6];
    memcpy(data_buf, data_plain, 6);
    for (int i = 0; i < 6; i++) {
        uint8_t a = (uint8_t)(0x00 + i);
        data_buf[i] = (data_buf[i] - lft[a & 7]) ^ lsb[a & 7];
    }
    bool data_ok = (memcmp(data_buf, data_cipher_expected, 6) == 0);

    printf("[wii_ext_crypto] selftest ft=%s sb=%s cal=%s data=%s (overall %s)\n",
           ft_ok ? "OK" : "BAD",
           sb_ok ? "OK" : "BAD",
           cal_ok ? "OK" : "BAD",
           data_ok ? "OK" : "BAD",
           (ft_ok && sb_ok && cal_ok && data_ok) ? "PASS" : "FAIL");
    if (!ft_ok)   wii_ext_crypto_print_diff("ft",   lft,      expected_ft, 8);
    if (!sb_ok)   wii_ext_crypto_print_diff("sb",   lsb,      expected_sb, 8);
    if (!cal_ok)  wii_ext_crypto_print_diff("cal",  cal_buf,  cal_cipher_expected, 16);
    if (!data_ok) wii_ext_crypto_print_diff("data", data_buf, data_cipher_expected, 6);
}
