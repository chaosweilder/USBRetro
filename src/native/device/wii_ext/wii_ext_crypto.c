// wii_ext_crypto.c - Wii Extension Controller Encryption
//
// Cipher for Wii extension controller reports. The Wiimote (or Wii console)
// sends a 16-byte key to extension registers 0x40-0x4F after writing 0xAA
// to register 0xF0. All subsequent register reads are encrypted.
//
// The cipher is a per-byte substitution: each byte is transformed using
// two 8-entry tables (ft and sb) indexed by address % 8:
//
//   encrypted = (plaintext - ft[addr % 8]) ^ sb[addr % 8]
//
// The ft and sb tables are derived from the 16-byte key using two 256-byte
// S-box lookup tables that are properties of the Wii hardware.
//
// Algorithm and S-box data from Hector Martin (marcan)'s 2008 reverse
// engineering of the Wii, published on wiibrew.org and sci.crypt.
// These are discovered hardware constants, not creative works.
//
// References:
//   https://wiibrew.org/wiki/Wiimote/Extension_Controllers
//   https://sci.crypt.narkive.com/g1zYewWd/reverse-engineering-the-wiimote-s-encryption
//
// SPDX-License-Identifier: Apache-2.0

